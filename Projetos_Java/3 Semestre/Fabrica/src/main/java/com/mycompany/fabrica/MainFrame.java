                    Image scaledImage = originalImage.getScaledInstance(30, 30, Image.SCALE_SMOOTH);  
                    ImageIcon robotIcon = new ImageIcon(scaledImage);
                    
                    label.setIcon(robotIcon);//Apos as manipulacoes necessarias, insere a imagem do robo no label
                    label.setBackground(Color.WHITE);
