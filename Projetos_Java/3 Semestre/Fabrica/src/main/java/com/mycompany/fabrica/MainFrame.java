    //Metodo para lidar com a interacao com o botao de posicionar robo
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {//Bloco try catch para manipulacao de erros
                    int i = Integer.parseInt(jTextField1.getText()); //Entrada da coordenada x do robo
                    int j = Integer.parseInt(jTextField2.getText()); //Entrada da coordenada y do robo
                    jTextField1.setText(""); //Limpar a caixa de texto
                    jTextField2.setText("");
                    if (fabrica.dentroDosLimites(i, j)) {
                        if(fabrica.getSala()[i-1][j-1]==2){// Se houver um obstaculo, aparece um aviso e o robo nao eh posicionado
                            JOptionPane.showMessageDialog(this, "Existe um obstáculo nessa posição", "Aviso", JOptionPane.WARNING_MESSAGE);
                        }else{//Posiciona o robo e atualiza o cenario
                                posicao.setIJ(i - 1, j - 1);
                                refreshGrid();
                        }
                            
                    } 
                    else {//Se a entrada for um valor inteiro mas estiver fora dos limites ou for negativa, aparece um aviso
                        JOptionPane.showMessageDialog(this, "Fora dos limites", "Aviso", JOptionPane.WARNING_MESSAGE);
                    }
                } catch (NumberFormatException ex) {//Caso tenha uma entrada invalida, aparece um aviso de erro
                    JOptionPane.showMessageDialog(this, "Entrada inválida", "Erro", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jTextField2.setText("");
                }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {//Bloco try catch para manipulacao de erros
                    int i = Integer.parseInt(jTextField3.getText());//Entrada da coordenada x do obstaculo
                    int j = Integer.parseInt(jTextField4.getText());//Entrada da coordenada x do obstaculo
                    jTextField3.setText("");//Limpar a caixa de texto
                    jTextField4.setText("");
                    if (fabrica.dentroDosLimites(i, j)) {
                        if(fabrica.getSala()[i-1][j-1]==2){// Se houver um obstaculo, aparece um aviso e o obstaculo nao eh posicionado
                            JOptionPane.showMessageDialog(this, "Já existe um obstáculo nessa posição", "Aviso", JOptionPane.WARNING_MESSAGE);
                        }
                        else if(fabrica.getSala()[i-1][j-1]==1){// Se houver um robo, aparece um aviso e o obstaculo nao eh posicionado
                            JOptionPane.showMessageDialog(this, "Já existe um robô nessa posição", "Aviso", JOptionPane.WARNING_MESSAGE);
                        }
                        else{//Posiciona o obstaculo e atualiza o cenario
                            obstaculos[fabrica.getQuantAtualObstaculos()] = new Obstaculos(fabrica);
                            obstaculos[fabrica.getQuantAtualObstaculos()].setIJ(i - 1, j - 1);
                            refreshGrid();
                        }
                        
                    } else {//Se a entrada for um valor inteiro mas estiver fora dos limites ou for negativa, aparece um aviso
                        JOptionPane.showMessageDialog(this, "Fora dos limites", "Aviso", JOptionPane.WARNING_MESSAGE);
                    }
                } catch (NumberFormatException ex) {//Caso tenha uma entrada invalida, aparece um aviso de erro
                    JOptionPane.showMessageDialog(this, "Entrada inválida", "Erro", JOptionPane.ERROR_MESSAGE);
                    jTextField3.setText("");//Limpar a caixa de texto
                    jTextField4.setText("");
                }
    }//GEN-LAST:event_jButton2ActionPerformed

